package fundamentos;

public class inferencia {
	public static void main(String[] args) {
		double a = 4.5;
		System.out.println(a);
		
		//var tenta interpretar o valor da variável
		var b = 4.5;
		System.out.println(b);
		
		var c = "Texto";
		System.out.println(c);
		
		//c = 4.5; 
		// não posso mudar o tipo da variável, se era uma string, ela fica "amarrada" a esse tipo
		
	}

}
